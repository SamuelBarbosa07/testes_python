#operadores aritimeticos 

a = 7
b = 2 
c = 4 
d = 14

print(f"a+b-c, o resultado sera de {a+b-c}")

print(f"a*b/c, o resultado sera de {a*b/c}")

print(f"c+22*d, o resultado sera de {c+22*d}")

print(f"a+b*c+d, o resultado sera de {a+b*c+d}")

print(f"(a+b)*(c+d), o resultado sera de {(a+b)*(c+d)}")

print(f"(d/a)/(b/c), o resultado sera de {(d/a)/(b/c)}")

print(f"10**a, o resultado sera de {10**a}")

print(f"58%a, o resultado sera de {58%a}")

print(f"(a+b+c*d)/(b-c*(b-c)), o resultado sera de {(a+b+c*d)/(b-c*(b-c))}")

print(f"(a+b+c*d)/(b-c*(b-c))+10**a, o resultado sera de {(a+b+c*d)/(b-c*(b-c))+10**a}")



